






with r   === DESC ORDER

arsort()  == a means sorting the values in DESC ORDER
krsort()  == k means sorting the keys  in DESC ORDER
rsort()  == normal arrays   in DESC ORDER


without r  ===  ASC ORDER

asort() == a means sorting the values  in ASC ORDER
ksort() == k means sorting the keys  in ASC ORDER
sort()  == normal arrays  in ASC ORDER

